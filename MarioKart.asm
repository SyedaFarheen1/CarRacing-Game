;---------------------------------------------------------
; Subroutine: drawStar
;---------------------------------------------------------
drawStar:
    push    bp
    mov     bp, sp
	
	; reset flag
	mov byte[F_starAlreadyHit], 0
	
    ; read parameters immediately (before pushing other regs)
    mov     dx, [bp+4]      ; y (word)
    mov     bx, [bp+6]      ; x (word)
	mov [star_x], bx 
	mov [star_y], dx
    ; save registers we will clobber
    push    ax
    push    di
    push    si
    push    cx

    ; prepare pointers/counters
    cld                     ; ensure DF = 0 so STOSB increments DI
    mov     si, star        ; source pixel data
    mov     cx, 24          ; 24 pixels per row (columns)
    mov     bp, 17           ; number of rows 

starRow:
    ; calculate offset = y*320 + x  => DI = y*320 + x
    mov     ax, dx
    shl     ax, 8           ; ax = y*256
    mov     di, dx
    shl     di, 6           ; di = y*64
    add     di, ax          ; di = y*320 (256+64)
    add     di, bx          ; di += x

    ; draw one row of 24 pixels
    push    cx              ; save row-column count if needed
    mov     cx, 24

starCol:
    mov     al, [si]
    cmp     al, 8           ; transparent color?
    je      .skipPixel
    stosb                   ; write AL to ES:DI and increment DI
    jmp     .nextPixel

.skipPixel:
    inc     di              ; skip pixel (advance in frame buffer)

.nextPixel:
    inc     si
    dec     cx
    jnz     starCol

    pop     cx              ; restore cx (rows remaining counter)
    inc     dx              ; y++
    dec     bp
    jnz     starRow

    ; restore registers
    pop     cx
