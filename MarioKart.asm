;---------------------------------------------------------
; subroutine that displays a single digit
;---------------------------------------------------------

displayDigit:
	push bp
	mov bp, sp
	push ax
	push bx
	push cx
	push dx
	push es
	push ds
	push si
	push di

	mov ax, 0xA000
	mov es, ax

	mov dx, [digity]		; y position - row (DX will be incremented per row)
    mov bx, [digitx]		; x position - col (BX used for base X)
    mov si, digits 			; base pointer to digit bitmap
    mov cx, 5       		; 5 rows

	mov ax, [bp+4]			; digit (word param)
	shl ax, 2				; digit * 4
	inc ax					; digit * 4 + 1  (your font start column)
	add si, ax				; SI -> first-row first-pixel of that digit

displayDigitRow:
    ; calculate offset = y*320 + x  -> store into DI for stosb
    mov di, dx
    shl di, 6               ; di = y*64
    mov ax, dx
    shl ax, 8               ; ax = y*256
    add di, ax              ; di = y*320
    add di, bx              ; di = y*320 + x

    mov bp, 3               ; 3 pixels per row

displayDigitCol:
    mov al, [si]            ; pixel color from font
    stosb                   ; write AL to ES:[DI], DI auto-inc
    inc si
    dec bp
    jnz displayDigitCol

    ; --- CRUCIAL FIX: after reading 3 bytes of this row, advance SI by 37
    ; because the font row stride is 40 and we've already consumed 3 bytes.
    add si, 37

    inc dx                  ; move to next row (Y + 1)
    loop displayDigitRow    ; repeat 5 rows

	pop di
	pop si
	pop ds
	pop es
	pop dx
	pop cx
	pop bx
	pop ax
	pop bp
	ret 2

