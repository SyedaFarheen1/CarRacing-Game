;---------------------------------------------------------
; game over sound
;---------------------------------------------------------
gameOverSound:
    push ax
    push bx
    push si
    push cx

    lea si, [endToneFreqs]
    mov cx, 3

go_loop:
    mov ax, [si]       ; AX = divisor
    add si, 2
    call playToneShort
    loop go_loop

    ; tiny wobble on the final low note: switch quickly between two nearby divisors
    mov ax, [si]       ; now AX = wobble1 (first word after the 3rd)
    call playToneShort
    mov ax, [si+2]     ; wobble2
    call playToneShort

    pop cx
    pop si
    pop bx
    pop ax
    ret
;---------------------------------------------------------
;playTone 
;---------------------------------------------------------
; AX = divisor (frequency)
playToneShort:
    push ax
    push dx
    push cx

    ; program PIT channel 2
    mov al, 0B6h
    out 43h, al

    out 42h, al        ; low
    mov al, ah
    out 42h, al        ; high

    ; enable speaker
    in   al, 61h
    or   al, 03h
    out  61h, al

    ; short delay (safe)
    mov cx, 500
toneOuter:
    mov dx, 200
toneInner:
    dec dx
    jnz toneInner
    loop toneOuter

    ; disable speaker
    in   al, 61h
    and  al, 0FCh
    out  61h, al

    pop cx
    pop dx
    pop ax
    ret

