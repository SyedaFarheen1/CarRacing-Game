Beep:
    push ax
    push dx

    ; Program PIT channel 2 for ~1000 Hz
    mov al, 0B6h         ; channel 2, lobyte/hibyte, mode 3
    out 43h, al

    mov ax, 1193         ; frequency divisor for ~1 kHz
    out 42h, al          ; send low byte
    mov al, ah
    out 42h, al          ; send high byte

    ; enable speaker output
    in   al, 61h
    or   al, 03h         ; enable speaker + gate
    out  61h, al

    ; delay so we can hear the beep
    mov cx, 200        ; outer loop
beepDelayOuter:
    mov dx, 300    ; inner loop
beepDelayInner:
	dec dx
	jnz beepDelayInner
    loop beepDelayOuter

    ; turn off speaker
    in   al, 61h
    and  al, 0FCh        ; disable speaker
    out  61h, al

    pop dx
    pop ax
    ret
	
