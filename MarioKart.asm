; =========================================================
; ========  RANDOM NUMBER GENERATOR (IMPROVED) ============
; =========================================================
; Input:  BL = max value (exclusive)
; Output: AL = random number 0..BL-1
; ---------------------------------------------------------
getRandom:
    push dx
    push cx

    mov ah, 00h
    int 1Ah              ; get system timer tick in CX:DX
    xor dx, cx           ; mix CX and DX for more randomness
    mov ax, dx
    xor al, [randCounter] ; mix in a small counter
    inc byte [randCounter]

    xor ah, ah
    div bl               ; AX / BL → AL=quotient, AH=remainder
    mov al, ah           ; AL = remainder (random 0..BL-1)

    pop cx
    pop dx
    ret

