;----------------------------------------------------------------  
; Subroutine that checks for collisions 
;---------------------------------------------------------------- 
checkCollision:
    push ax
    push bx
    push cx
    push dx
    push si
    push di
    push es

    ; Set video segment
    mov ax, 0A000h
    mov es, ax
	;collisions if:
	;if tire1 -1y or tire1 -3x is not grey or white (checks front and left side) 
	;if tire2 -1y or tire2 + 3x is not grey or white (checks front and right side) 
	;if tire3 +1y or tire3 -4x is not grey or white (checks back and left side) 
	;if tire4 +1y or tire4 +4x is not grey or white (checks back and right side)
	;check if star collision, if + or - 4 pixels has colour 11, then is star
	;if is star, continue to move, increase speed of scrolling by decreasing the delay
	
    ;------------------------------------------
    ; Check Tire 1 (front-left)
    ;------------------------------------------
	;check y - 1
    mov bx, [C_tire1x]
    mov dx, [C_tire1y]
    dec dx                  ; y - 1 (front)
    call checkPixel

    cmp al, [roadColor1]    ; compare with grey
    je noCollision_t1
    cmp al, [roadColor2]    ; compare with white
    je noCollision_t1
    jmp yesCollision

noCollision_t1:

    ;------------------------------------------
    ; Check Tire 2 (front-right)
    ;------------------------------------------
    mov bx, [C_tire2x]
    mov dx, [C_tire2y]
    dec dx				;y - 1 (front)
    call checkPixel
    cmp al, [roadColor1]
    je noCollision_t2
    cmp al, [roadColor2]
    je noCollision_t2
    jmp yesCollision

noCollision_t2:

    ;------------------------------------------
    ; Check Tire 3 (back-left)
    ;------------------------------------------
    mov bx, [C_tire3x]
    mov dx, [C_tire3y]
    inc dx                  ; y + 1 (back)
    call checkPixel
    cmp al, [roadColor1]
    je noCollision_t3
    cmp al, [roadColor2]
    je noCollision_t3
    jmp yesCollision

noCollision_t3:

    ;------------------------------------------
    ; Check Tire 4 (back-right)
    ;------------------------------------------
    mov bx, [C_tire4x]
    mov dx, [C_tire4y]
    inc dx				;y + 1 (back)
    call checkPixel
    cmp al, [roadColor1]
    je noCollision_t4
    cmp al, [roadColor2]
    je noCollision_t4
    jmp yesCollision

noCollision_t4:
    jmp noCollision
	
yesCollision:
	mov byte [F_isStar], 0
	call checkIsStar		; check if collision is with star
	mov al, [F_isStar]		
	cmp al, 0				; if flag not set it is not star
	je obstacleCollision
	
starDetected:
    ; mark for main-loop handling instead of doing heavy work here
    mov byte [needHandleStar], 1
    ; optionally save star coordinates or lane if needed
    jmp doneCollision

obstacleCollision:
    ; mark crash for main loop, don't wait here
    mov byte [needHandleCrash], 1
    jmp doneCollision

noCollision:
    ; no collision detected
    ; continue game loop

doneCollision:
    pop es
    pop di
    pop si
    pop dx
    pop cx
    pop bx
    pop ax
    ret

;------------------------------------------
; subroutine to check if is star
;------------------------------------------
checkIsStar:
	push ax
    push bx
    push cx
    push dx
    push si
    push di
    push es
	
	; Set video segment
    mov ax, 0A000h
    mov es, ax

;------------------------------------------
; Check Tire 1 (front-left)
;------------------------------------------
	;check y - 1
    mov bx, [C_tire1x]
    mov dx, [C_tire1y]
    dec dx                  ; y - 1 (front)
	add bx, 3				;because star is smaller
    call checkPixel

    cmp al, [starColor]
    je near starCollision	; near jump needed instead of short

;------------------------------------------
; Check Tire 2 (front-right)
;------------------------------------------
    mov bx, [C_tire2x]
    mov dx, [C_tire2y]
	dec dx                 ; y - 1 (front)
	sub bx, 3
	call checkPixel

    cmp al, [starColor]
    je near starCollision	; near jump needed instead of short
	
	jmp notStarCollision
	
starCollision:
	; set flag to 1
	mov byte[F_isStar], 1
	
	cmp byte[F_starAlreadyHit], 1
	je notStarCollision

	mov byte[F_starAlreadyHit], 1

    ; update score once
    add word [score], 10

notStarCollision:
	pop es
    pop di
    pop si
    pop dx
    pop cx
    pop bx
    pop ax
    ret

