	restore2ndBananaPeel:
	push word [banana_X]
	push word [banana_Y]
	call drawBananaPeel
	jmp doneRestore
	
doneRestore:
	mov byte [scrollCount], 0
	call drawBorder
	mov byte [needRedrawFuel], 1
	jmp gameLoop

;----------------------------------------------------------------
; RESTART
;----------------------------------------------------------------

restart:
	call uninstallTimerISR
	call uninstallKbisr
	mov byte [F_introScreen], 1
	mov word [score], 0 
	mov word [kart_X], 148
	mov word [kart_Y], 170
	mov byte[fuelBarColsLeft], 64
	call start 
	
;----------------------------------------------------------------
; START
;----------------------------------------------------------------

start:
	; Setting video mode 13h
    mov ax, 13h
    int 10h		; switching to 320x200 graphics mode
	call installKbisr		; this will allow key presses to be detected
	
	call introScreen
	call gameStartSound
introScreenLoop:
	
	mov ax, [F_introScreen]
	cmp ax, 1
	je introScreenLoop
		
	call loadingScreen
	call displayBackground
	call drawBorder
	
	;have to put this just before game loop so fuel bar will decrease from the beginning
	call installTimerISR	;this will allow for the timer to be used in decreasing fuel bar 
	mov byte[fuelBarColsLeft], 64
;----------------------------------------------------------------
; GAME LOOP
;----------------------------------------------------------------

gameLoop:
	; check fuel
	cmp byte[F_reduceFuel], 1
	jne skipReduceFuel
	mov byte[F_reduceFuel], 0
	call reduceFuel
	
skipReduceFuel:
	call drawKart24

	; Check for pause
	cmp byte [F_pauseScreen], 1
	jne noPause
	
	; save state 
	mov ax, [car1_x]
	mov [SAVEcar1_X], ax
	mov ax, [car1_y]
	mov [SAVEcar1_y], ax
	mov ax, [car2_x]
	mov [SAVEcar2_x], ax
	mov ax, [car2_y]
	mov [SAVEcar2_y], ax
	mov ax, [banana_X]
	mov [SAVEbanana_X], ax
	mov ax, [banana_Y]
	mov [SAVEbanana_Y], ax
	mov ax, [star_x]
	mov [SAVEstar_x], ax
	mov ax, [star_y]
	mov [SAVEstar_y], ax
	
	call pauseScreen
	
 noPause:
    ; Check to generate obstacles
	; Flag set in timer ISR, since generating obstacles depends on scrolling
	cmp byte[F_ObstacleGeneration], 1
	jne SkipObstacleGen
    call GenerateObstacles
	mov byte [F_ObstacleGeneration], 0
		
SkipObstacleGen:
	; Check if tire coordinates need to be updated
	cmp byte[F_updateTires], 1
	jne skipUpdateTires
	call getTires					; update tire coordinates
	mov byte[F_updateTires], 0
	
skipUpdateTires:
    ; check collisions (sets flags instead of doing heavy work)
    call checkCollision

    ; ---- handle star collision safely ----
    cmp byte [needHandleStar], 0
    je .no_star
        mov byte [needHandleStar], 0
        call increaseFuel_fast

	.redraw_frame_loop:
		cmp byte [needRedrawFuel], 0
		je .skipFuelRedraw

		call drawBorder
		mov byte [needRedrawFuel], 0 	
		
.skipFuelRedraw:
		;erase star
        ;let main loop redraw gradually (call once per frame)
		
.no_star:

    ; ---- handle obstacle collision safely ----
    cmp byte [needHandleCrash], 0
    je .no_crash
        mov byte [needHandleCrash], 0
		call Beep
        jmp endGame 
        ; or show crash screen, etc.
.no_crash:

    ; update score
    ; call displayScore
	cmp byte [fuelBarColsLeft], 0
    je endGame 
    jmp gameLoop

;----------------------------------------------------------------
; END OF GAME LOOP
;----------------------------------------------------------------
endGame:
	call uninstallTimerISR	;retore old timer ISR
	call gameOverSound
	call exitScreen
	
exitScreenLoop:
	
	mov ax, [F_exitScreen]
	cmp ax, 1
	je exitScreenLoop
	
leaveGame:

	call uninstallKbisr		; restore old kbisr
	
	; return to text mode
    mov ax, 03h
    int 10h
	
	; terminate program
    mov ax, 4C00h
    int 21h