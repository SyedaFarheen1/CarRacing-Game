; 1) write our own ISR
; 2) install our ISR by replacing default interrupt vector in the Interrupt Vector Table (IVT) with the address of your ISR.
; 3) Each time the timer ticks, our ISR will run automatically	
; 4) When done, you must call the old handler so the system continues to run properly.

;----------------------------------------------------------------  
; Subroutine that installs timer ISR at int 08h
;---------------------------------------------------------------- 

installTimerISR:
	cli		; clear interrupts
	push es
	xor ax, ax
	mov es, ax
	
	;save old interrupt - int 08h -> 8*4
	mov ax, [es:8*4]
	mov [oldTimerISR], ax	; old offset
	mov ax, [es:8*4+2]		
	mov [oldTimerISR+2], ax	; old segment
	
	;install our timer isr
	mov word[es:8*4], timerISR	;offset
	mov [es:8*4+2], cs			;segment
	
	pop es
	sti		; set interrupts
	ret
	
;----------------------------------------------------------------  
; Timer ISR - int 08h
;---------------------------------------------------------------- 

timerISR:
    ; save state
    push ax
    push bx
    push cx
    push dx
    push si
    push di
    push bp
    push ds
    push es
	
    ; ds = cs - so we can access variables
    mov ax, cs
    mov ds, ax

	;---------------------------------------------------------
	; handle fuel
	;---------------------------------------------------------
    ;if tick count is 10, decrease fuel bar and reset tick count
    inc byte [tickCount]
    cmp byte [tickCount], 10
    jb skipDecreaseFuelBar
    mov byte [tickCount], 0
	
	mov byte[F_reduceFuel], 1
	
	;---------------------------------------------------------
	; handle scrolling
	;---------------------------------------------------------

skipDecreaseFuelBar:

	call moveScreen
	cmp byte [F_ObstacleGeneration], 0
	je incrementYCoord
	jmp checkStarIncrement
	
incrementYCoord:
	inc byte[car1_y]
	inc byte [car2_y]
	inc byte [banana_Y]	
	
checkStarIncrement:
	cmp byte [starSpawnedFlag], 0
	je incrementStarYCoord
	jmp moveOnToScroll

incrementStarYCoord:
	inc byte [star_y]
	
moveOnToScroll:
	; ----- obstacle generation after each scroll -----
	inc byte [scrollCount]
	cmp byte [scrollCount], 90
	jne doneTimerISR
	mov byte[F_ObstacleGeneration], 1
	mov byte [scrollCount], 0

doneTimerISR:
    ; unhooking interrupt - returning to old handler
	pushf					; push flags register
	call far [oldTimerISR]	; call old timer isr
	
	; send EOI to PIC so more IRQs can arrive
    mov al, 20h
    out 20h, al
    
	; restore registers
    pop es
    pop ds
    pop bp
    pop di
    pop si
    pop dx
    pop cx
    pop bx
    pop ax
    iret

;----------------------------------------------------------------  
; subroutine that uninstalls timer ISR
;----------------------------------------------------------------
uninstallTimerISR:
	cli		;clear interrupts
	
	push es
	xor ax, ax
	mov es, ax
	
	mov ax, [oldTimerISR]
	mov [es:8*4], ax		; offset
	mov ax, [oldTimerISR + 2]
	mov [es:8*4 + 2], ax	; segment 
	
	pop es
	sti		;set interrupts
	ret
	
;----------------------------------------------------------------  
; Subroutine that reduces fuel 
;---------------------------------------------------------------- 
reduceFuel:
    ; size of fuel bar is 7 rows by 64 cols
    ; decrease fuel bar with time
    ; every 20 columns reduced → generateRandomStar
    ; each column = ~0.47s (10 interrupts × 18.2 Hz)
    
    push bp
    mov bp, sp
    push ax
    push bx
    push cx
    push dx
    push es
    push ds
    push si
    push di
    
    mov ax, 0A000h
    mov es, ax
    
    mov al, [fuelBarColsLeft]
    cmp al, 0
    je fuelBarEmpty
    
    ;----------------------------------------------
    ; Reduce number of filled columns
    ;----------------------------------------------
    dec byte [fuelBarColsLeft]

    ;----------------------------------------------
    ; Check if we should spawn a star every 20 cols
    ;----------------------------------------------
    mov al, [fuelBarColsLeft]   ; remaining columns
    mov ah, 0
    mov bl, 25
    div bl                      ; AL / 25 → AH = remainder
    cmp ah, 0                   ; remainder == 0?
    jne skipStarCheck           ; not multiple of 25 → skip

    cmp byte [starSpawnedFlag], 0
    jne skipStarCheck           ; already spawned this tick → skip

    call generateRandomStar
    mov byte [starSpawnedFlag], 1
    jmp afterStarCheck

skipStarCheck:
    ; reset the flag once we’re no longer on a multiple of 25
    mov al, [fuelBarColsLeft]
    mov ah, 0
    mov bl, 25
    div bl
    cmp ah, 0
    je afterStarCheck
    mov byte [starSpawnedFlag], 0

afterStarCheck:
    ;----------------------------------------------
    ; Clear one column visually (right to left)
    ;----------------------------------------------
    xor bh, bh        ; clear high byte of BX
    mov bl, [fuelBarColsLeft]
    add bx, [fuelBarX]
    
    mov dx, [fuelBarY]        ; starting row
    mov cx, 7                 ; 7 rows tall
    
fuelBarNextRow:
    ; calculate offset = y*320 + x
    mov di, dx
    shl di, 6
    mov ax, dx
    shl ax, 8
    add di, ax
    add di, bx

    mov al, 1                 ; blue background
    mov [es:di], al

    inc dx
    loop fuelBarNextRow
    jmp doneReduce

fuelBarEmpty:
    ; game over - keep paused until key pressed (demo)
    mov ah, 0
    int 16h

doneReduce:
    pop di
    pop si
    pop ds
    pop es
    pop dx
    pop cx
    pop bx
    pop ax
    pop bp
    ret
;----------------------------------------------------------------  
; Subroutine that increases fuel (short)
;---------------------------------------------------------------- 
; increaseFuel_fast: just update logic, set redraw flag
increaseFuel_fast:
    push ax
    push bx
	
    mov al, [fuelBarColsLeft]     ; old
    mov bl, al                    ; backup
    add al, 12                    ; add 12 cols

    cmp al, 64
    jbe .store
    mov al, 64

.store:
    cmp al, bl                    ; if no actual change, skip
    je .done

    mov [fuelBarColsLeft], al     ; update
    mov byte [needRedrawFuel], 1

.done:
    pop bx
    pop ax
    ret

; kbisr 
; 1. save registers
; 2. read scancode from port 60h
; 3. update car position, for example
; 4. send EOI to PIC (port 20h)
; 5. call old ISR (unhooking interrupt)
; 6. restore registers + iret

;----------------------------------------------------------------  
; Subroutine that installs keyboard ISR at int 09h
;---------------------------------------------------------------- 

installKbisr:
	cli			;clear interrupts
	push es
	xor ax, ax
	mov es, ax
	
	; save old kbisr
	mov ax, [es:9*4]	;offset
	mov [oldKbisr], ax
	mov ax, [es:9*4+2]	;segment
	mov [oldKbisr+2], ax
	
	; install our kbisr
	mov word[es:9*4], kbisr
	mov word[es:9*4+2], cs
	
	pop es
	sti		; set interrupts
	ret

;----------------------------------------------------------------  
; Keyboard ISR - int 09h  (modified)
;----------------------------------------------------------------  
kbisr:
    push ax
    push bx
    push cx
    push dx
    push si
    push di
    push bp
    push ds
    push es

	call click
	
    ; ds = cs to access variables
    mov ax, cs
    mov ds, ax

    ; read scancode from port 60h
    in al, 60h
    mov bl, al    ; scancode stored in bl

    ; ignore key-release (break) codes (set1: add 80h on key release)
    test al, 80h
    jnz near .doneISR        ; ignore break codes

    ; If we are currently paused, handle pause Y/N immediately
    cmp byte [F_pauseScreen], 1
    je near .checkPauseYN

; press enter on intro screen to play
    cmp bl, 1Ch      ; Enter key
    jne .checkMovement
    cmp word [F_introScreen], 1
    jne near .doneISR
    mov word[F_introScreen], 0
    jmp near .doneISR

; movement keys
.checkMovement:
    cmp bl, 4Bh      ; Left arrow
    je .moveLeft
    cmp bl, 4Dh      ; Right arrow
    je .moveRight
    ; fall through to pause check if not movement
    jmp .checkPause

.moveLeft:
    mov byte [F_clearKart], 1
	mov byte [F_updateTires], 1
    mov ax, [kart_X]
    mov [oldkart_X], ax
    mov ax, [kart_Y]
    mov [oldkart_Y], ax

    mov al, [currentLane]
    cmp al, 1
    je near .doneISR      ; can't move left
    dec byte [currentLane]
    mov ax, [kart_X]
    sub ax, [laneDifference]
    mov [kart_X], ax
    jmp near .doneISR

.moveRight:
    mov byte [F_clearKart], 1
	mov byte [F_updateTires], 1
    mov ax, [kart_X]
    mov [oldkart_X], ax
    mov ax, [kart_Y]
    mov [oldkart_Y], ax

    mov al, [currentLane]
    cmp al, 3
    je near .doneISR      ; can't move right
    inc byte [currentLane]
    mov ax, [kart_X]
    add ax, [laneDifference]
    mov [kart_X], ax
    jmp near .doneISR

; pause key (ESC)
.checkPause:
    cmp bl, 01h          ; ESC key (make)
    jne .checkExit
    cmp byte [F_pauseScreen], 1
    je near .doneISR          ; already paused, ignore (we handle Y/N above)
    mov byte [F_pauseScreen], 1
    jmp near .doneISR         ; we set the paused flag now and return
		
; pause screen Y/N (now reachable on any ISR when F_pauseScreen=1)
.checkPauseYN:
    cmp byte [F_pauseScreen], 1
    jne .checkExit        ; fallback if paused flag was cleared elsewhere

    cmp bl, 15h           ; Y key (make - set1)
    je .pauseYes
    cmp bl, 31h           ; N key (make - set1)
    je .pauseNo
    jmp .doneISR

.pauseYes:
    mov byte [pauseChoice], 1
    jmp .doneISR

.pauseNo:
    mov byte [pauseChoice], 2
    mov byte [F_exitScreen], 0     ; <---- ADD THIS
    mov byte [exitChoice], 0       ; <---- ADD THIS
    jmp .doneISR


; exit screen keys
.checkExit:
	cmp byte [F_pauseScreen], 1
    je .doneISR        ; if pause active, don't handle exit keys here
    cmp byte [F_exitScreen], 1
    jne .doneISR     ; only check if exit screen is active

    cmp bl, 15h       ; Y key
    je .yesChoice
    cmp bl, 31h       ; N key
    je .noChoice
    jmp .doneISR

.yesChoice: 
	mov byte [exitChoice], 1 
	jmp near .doneISR 
	
.noChoice: 
	mov byte [exitChoice], 2 
	jmp near .doneISR

; done ISR, call old ISR & EOI
.doneISR:
    pushf
    call far [oldKbisr]
    mov al, 20h
    out 20h, al

    pop es
    pop ds
    pop bp
    pop di
    pop si
    pop dx
    pop cx
    pop bx
    pop ax
    iret

